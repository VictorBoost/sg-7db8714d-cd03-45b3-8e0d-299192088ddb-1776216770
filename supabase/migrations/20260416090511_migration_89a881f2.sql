-- Staff Members table
CREATE TABLE IF NOT EXISTS staff_members (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  provider_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  email TEXT NOT NULL,
  password_hash TEXT NOT NULL,
  role TEXT NOT NULL CHECK (role IN ('accept_bids', 'manage_accounts', 'other')),
  custom_role_label TEXT,
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE INDEX idx_staff_members_provider ON staff_members(provider_id);
CREATE INDEX idx_staff_members_active ON staff_members(is_active);

COMMENT ON TABLE staff_members IS 'Staff members added by service providers to help manage their business';

-- RLS for staff_members (T1: provider owns their staff)
ALTER TABLE staff_members ENABLE ROW LEVEL SECURITY;

CREATE POLICY "providers_view_own_staff" ON staff_members
  FOR SELECT USING (auth.uid() = provider_id);

CREATE POLICY "providers_create_staff" ON staff_members
  FOR INSERT WITH CHECK (auth.uid() = provider_id);

CREATE POLICY "providers_update_own_staff" ON staff_members
  FOR UPDATE USING (auth.uid() = provider_id);

CREATE POLICY "providers_delete_own_staff" ON staff_members
  FOR DELETE USING (auth.uid() = provider_id);

-- Accounting Entries table
CREATE TABLE IF NOT EXISTS accounting_entries (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  provider_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  entry_type TEXT NOT NULL CHECK (entry_type IN ('income', 'expense')),
  amount NUMERIC NOT NULL CHECK (amount > 0),
  gst_included BOOLEAN DEFAULT false,
  description TEXT NOT NULL,
  entry_date DATE NOT NULL,
  contract_id UUID REFERENCES contracts(id) ON DELETE SET NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE INDEX idx_accounting_entries_provider ON accounting_entries(provider_id);
CREATE INDEX idx_accounting_entries_date ON accounting_entries(entry_date DESC);
